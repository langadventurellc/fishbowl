/**
 * LayoutVariant union for container component layout options.
 *
 * Defines the supported layout variants that control spacing and visual
 * presentation of container components in the input area.
 *
 * @module types/ui/components/LayoutVariant
 */

/**
 * Layout variants for container components.
 * - "default": Standard spacing and layout for normal use
 * - "compact": Reduced spacing for smaller screens or constrained layouts
 */
export type LayoutVariant = "default" | "compact";
